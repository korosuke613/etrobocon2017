#define ASCII_PRINT_FIRST (0x20)
#define ASCII_PRINT_LAST  (0x7E)
#define ASCII_PRINT_NUM   (ASCII_PRINT_LAST-ASCII_PRINT_FIRST+1)

static font_t   font_w6h8;
static bitmap_t font_w6h8_array[ASCII_PRINT_NUM];
static uint8_t  font_w6h8_pixels[ASCII_PRINT_NUM][BITMAP_PIXELS_SIZE(6,8)];

static font_t   font_w10h16;
static bitmap_t font_w10h16_array[ASCII_PRINT_NUM];
static uint8_t  font_w10h16_pixels[ASCII_PRINT_NUM][BITMAP_PIXELS_SIZE(10,16)];

static bool_t lejos_get_pixel(uint8_t *data, int w, int h, int x, int y) {
    if (x < 0 || x >= w) return false;
    if (y < 0 || y >= h) return false;

    int line_width = (w+7)/8;
    uint8_t pixelbyte = data[line_width * y + x / 8];
    return pixelbyte & (0x1 << (x % 8));
}

void initialize_lcd_font() {
    // Initialize font_w6h8_array
    for (int i = 0; i < ASCII_PRINT_NUM; ++i) {
        font_w6h8_array[i].width = 6;
        font_w6h8_array[i].height = 8;
        font_w6h8_array[i].pixels = font_w6h8_pixels[i];
    }

    // Initialize font_w6h8_pixels
    for (int i = 0; i < ASCII_PRINT_NUM; ++i) {
        bitmap_t *bitmap = font_w6h8_array + i;
        for (int y = 0; y < bitmap->height; ++y) {
            for (int x = 0; x < bitmap->width; ++x) {
                bitmap_set_pixel(bitmap, x, y, lejos_get_pixel(font_w6h8_lejos, 6 * 96, 8, i * bitmap->width + x, y));
            }
        }
    }

    // Set font information
    font_w6h8.width = 6;
    font_w6h8.height = 8;
    font_w6h8.array_sz = ASCII_PRINT_NUM;
    font_w6h8.array = font_w6h8_array;
    font_w6h8.dict = NULL;
    font_w6h8.dict_sz = 0;
    font_w6h8.first_code_point = ASCII_PRINT_FIRST;

    // Initialize font_w10h16_array
    for (int i = 0; i < ASCII_PRINT_NUM; ++i) {
    	font_w10h16_array[i].width = 10;
    	font_w10h16_array[i].height = 16;
    	font_w10h16_array[i].pixels = font_w10h16_pixels[i];
    }


    // Initialize font_w10h16_pixels
    for (int i = 0; i < ASCII_PRINT_NUM; ++i) {
        bitmap_t *bitmap = font_w10h16_array + i;
        for (int y = 0; y < bitmap->height; ++y) {
            for (int x = 0; x < bitmap->width; ++x) {
                bitmap_set_pixel(bitmap, x, y, lejos_get_pixel(font_w10h16_lejos, bitmap->width * 96, bitmap->height, i * bitmap->width + x, y));
            }
        }
    }

    // Set font information
    font_w10h16.width = 10;
    font_w10h16.height = 16;
    font_w10h16.array_sz = ASCII_PRINT_NUM;
    font_w10h16.array = font_w10h16_array;
    font_w10h16.dict = NULL;
    font_w10h16.dict_sz = 0;
    font_w10h16.first_code_point = ASCII_PRINT_FIRST;

    global_brick_info.font_w6h8 = &font_w6h8;
    global_brick_info.font_w10h16 = &font_w10h16;
}

